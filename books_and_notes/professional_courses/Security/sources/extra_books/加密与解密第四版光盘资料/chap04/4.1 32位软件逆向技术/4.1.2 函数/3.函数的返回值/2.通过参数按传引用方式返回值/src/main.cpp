#include <stdio.h>

void max(int *a, int *b);
main( )
{
	int a=5,b=6;
	max(&a, &b); 
	printf("a、b中较大的数是%d",a); //将最大的数显示出来
	return 0;
}

void max( int *a, int *b) 
{ 
	if(*a < *b)
		*a=*b; 		//经比较后，将较大的数放到a变量之中
}
