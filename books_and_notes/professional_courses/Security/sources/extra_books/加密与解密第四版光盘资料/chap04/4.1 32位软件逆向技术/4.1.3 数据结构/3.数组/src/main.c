#include <stdio.h>

int main(void)
{
	static int a[3]={0x11,0x22,0x33};
    int i,s=0,b[3];
	for(i=0;i<3;i++)
	{ 
		s=s+a[i];
		b[i]=s;
	}
	
	for(i=0;i<3;i++)
	{
		printf("%d\n",b[i]);
	}
	
	return 0;
}
